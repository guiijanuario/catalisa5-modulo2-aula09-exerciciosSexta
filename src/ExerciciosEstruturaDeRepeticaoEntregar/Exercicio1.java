package ExerciciosEstruturaDeRepeticaoEntregar;

public class Exercicio1 {
    public static void main(String[] args) {
        for(int i = 0; i <=100; i++){
            System.out.println("Mostrando na tela a sequência com for: " + i);
        }

        System.out.println("[----------------------------------------]");


        int i = 0;

        while (i <= 100){
            System.out.println("Mostrando na tela a sequência com while: " + i);
            i++;
        }
    }
}
